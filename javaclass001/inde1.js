/*O comando 'alert' dispara uma mensagem na tela "olá mundo!" que aparecerá junto a um botão "ok" de confirmação.*/
alert ('olá mundo!')
/* o "confirm" dispara uma outra mensagem na tela "Está gostando de Java Script?" com botão "ok e cancelar".*/
confirm ('Está gostando de Java Script?')
/* o 'prompt' dispara uma mensagem na tela "Qual é o seu nome" e em seguida cria um input na tela com botão de "ok e cancelar".*/
prompt ('Qual é o seu nome?')